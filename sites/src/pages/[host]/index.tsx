import request, { GraphQLClient } from "graphql-request";
import Head from "next/head";
import { Constants } from "../../config";
import { GET_RESTAURANT_INFO } from "../../graphql/restaurant";

export default function Home({ host, data }: any) {
  console.log(data);

  return (
    <>
      <Head>
        <title>{data ? data.name : "TezzBites Partner Website"}</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <h1>{data ? data.name : null} - Home Page</h1>
      <h2>Host: {host}</h2>
    </>
  );
}

export async function getStaticProps(context: any) {
  const host = context.params.host;

  const endpoint = Constants.GraphQL_API;

  const graphQLClient = new GraphQLClient(endpoint);

  const fetchData = await graphQLClient.request(GET_RESTAURANT_INFO, {
    domain: host,
  });

  const data = fetchData.restaurantConfig
    ? fetchData.restaurantConfig.Restaurant[0]
    : null;

  console.log(data);

  return {
    props: {
      host,
      data,
    },
    // revalidate: 60,
  };
}

export async function getStaticPaths() {
  return {
    paths: [],
    fallback: "blocking",
  };
}
